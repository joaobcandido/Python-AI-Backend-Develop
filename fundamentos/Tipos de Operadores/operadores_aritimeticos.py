# conhecendo os operadores aritimeticos
# adição
print("adicao 1 + 1 = ",1 + 1)
# subtração
print("subtracao 2 - 1 = ",2 - 1)
# multiplicação
print("multiplicacao 2 * 2 = ",2 * 2)
# divisão
print("divisao 10 / 3 = ",10 / 3)
# divisão inteira
print("divisao inteira 10 // 3 = ",10 // 3)
# módulo (resto da divisão)
print("modulo 10 % 3 = ",10 % 3)
# exponenciação
print("exponenciacao 2 ** 3 = ",2 ** 3)


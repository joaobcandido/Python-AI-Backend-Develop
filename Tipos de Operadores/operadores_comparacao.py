#operadores de comparação são utilizados para comparar dois valores
saldo = 300
saque = 200
# operador de igualdade
print("saldo eh igual a saque ?",saldo == saque)
# operador de diferença
print("saldo eh diferente de saque ?",saldo != saque)
# operador maior que/ maior ou igual
print("saldo eh maior de saque ?",saldo > saque)
print("saldo eh maior ou igual a saque ?",saldo >= saque)
# operador menor que/ menor ou igual
print("saldo eh menor de saque ?",saldo < saque)
print("saldo eh menor ou igual a saque ?",saldo <= saque)
